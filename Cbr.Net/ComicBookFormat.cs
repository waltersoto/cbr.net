namespace Cbr.Net
{
    /// <summary>
    /// Supported comic book container formats.
    /// </summary>
    public enum ComicBookFormat
    {
        /// <summary>RAR based container (.cbr).</summary>
        Cbr,
        /// <summary>ZIP based container (.cbz).</summary>
        Cbz,
        /// <summary>TAR based container (.cbt).</summary>
        Cbt,
        /// <summary>7-Zip based container (.cb7).</summary>
        Cb7,
        /// <summary>ACE based container (.cba).</summary>
        Cba
    }
}
